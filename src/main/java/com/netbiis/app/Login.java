    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) throws SQLException {//GEN-FIRST:event_jButton1ActionPerformed
        String nome = jTextField1.getText();
        String cpf = jTextField2.getText();
        
        try {
        	Cliente cliente = cf.findByName(nome);
            
            	if (cliente.getClienteName().equals(nome) && cliente.getClienteCpf().equals(cpf)) {
                    Pedido frame = new Pedido(cliente.getIdCliente());
                    frame.setVisible(true);
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(null, "Senha incorreta tente de novo");
                }
			
            
		} catch (Exception e) {
			JOptionPane.showMessageDialog(null, "User não existe");
		}
    }//GEN-LAST:event_jButton1ActionPerformed

